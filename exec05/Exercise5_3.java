package exec05;

public class Exercise5_3 {
	/*
	 * 関数名:main
	 * 概要:論理型変数の値を表示する
	 * 引数:なし
	 * 戻り値:なし
	 * 作成者:hagiwara
	 * 作成日:2023/04/02
	 */
  public static void main(String[] args) {
    // 論理型の変数truthValueにtrueを代入
    boolean truthValue = true;
    // truthValueの値を表示
    System.out.println("truthValue = " + truthValue);

    // 論理型の変数falseValueにfalseを代入
    boolean falseValue = false;
    // falseValueの値を表示
    System.out.println("falseValue = " + falseValue);
  }
}
