public class Exercise5_3 {
	/*
	 * 関数名:main
	 * 概要:論理型変数の値を表示する
	 * 引数:なし
	 * 戻り値:なし
	 * 作成者:N.Hagiwara
	 * 作成日:2024/04/03
	 */
	public static void main(String[] args) {
		// 論理型の変数にtrueを代入
		boolean isTrue = true;
		// trueを格納する変数の値を表示
		System.out.println("isTrue = " + isTrue);

		// 論理型の変数にfalseを代入
		boolean isFalse = false;
		// falseValueを格納する変数の値を表示
		System.out.println("isFalse = " + isFalse);
	}
}
